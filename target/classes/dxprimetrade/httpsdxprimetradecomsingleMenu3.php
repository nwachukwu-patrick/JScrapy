        <p>Financial institutions and professional financial advisers offer a host of investment services that are focused on helping their clients meet predetermined financial goals. These investment services may be customized to a clientâ€™s income levels, circumstances and objectives and focus on wealth creation and protection. Investment services include making, organizing and managing investments and may also encompass financial planning, accounting, life insurance and tax planning.<br> <br> <br> Types of Investment Services<br> Prior to the late 1990s, US commercial banks were forbidden by law to offer investment services. The Gramm-Leach-Bliley Act of 1999 made it legal for a bank to offer both commercial and investment services. Over the years, the distinction between commercial and investment banks has blurred. The types of investment services offered by banks include:<br> <br> Money management: The first step for any investor is to ascertain the amount of money he has at his disposal for investing and the various financial options available to maximize returns and tax rebates and minimize risks.<br> <br> <br> Portfolio management services: These services are aimed at investors achieving the right mix of investments, such as stocks, bonds, commodities, derivatives and CDs, depending upon their financial goals and risk appetite. Portfolio management encompasses making a choice between equity and debt, domestic and international investments as well as growth and safety.<br> <br> <br> Retirement planning services: Once the retirement goals have been laid down, appropriate investments need to be made into pension plans, 401(k) plans and profit sharing plans. One may also need to invest into the financial and money markets to ensure a steady stream of income post retirement.<br> <br> <br> Collective investment portfolio management: Banks offer advice on collective investments, such as mutual funds, ETFs and managed funds. One may also need professional advice for investing in hedge funds. Such funds hedge their risks via methods like short selling.<br> <br> <br> Brokerage services: Investors need brokers to trade in bonds, stocks, foreign currency and derivatives. The brokerage services offered by banks are considered more reliable and are preferred to those offered by individual brokers.<br> <br> <br> Apart from financial goals, investors may have other priorities that financial institutions providing investment services can take into account. Such priorities may include socially responsible investing, green investing and sustainable investing.</p>
